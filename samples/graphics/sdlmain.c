any LISP_SDL_PollEvent(Context *CONTEXT_PTR, any x)
{
    SDL_Event event;
    if (SDL_PollEvent( &event ))
    {
        cell c1, c2;

        struct bn *n = (struct bn*)malloc(sizeof(struct bn));
        bignum_from_int(n, event.type);
        NewNumber(n, r1);
        Push(c1, r1);

        if (event.type == SDL_WINDOWEVENT)
        {
            n = (struct bn*)malloc(sizeof(struct bn));
            bignum_from_int(n, event.window.event);
            NewNumber(n, r2);
            Push(c2, r2);

            any result = cons(CONTEXT_PTR, data(c1), cons(CONTEXT_PTR, data(c2), Nil));
            drop(c1);
            return result;
        }
        else if (event.type == SDL_TEXTINPUT)
        {
            n = (struct bn*)malloc(sizeof(struct bn));
            bignum_from_int(n, ((char *)event.text.text)[0]);
            NewNumber(n, r2);
            Push(c2, r2);

            any result = cons(CONTEXT_PTR, data(c1), cons(CONTEXT_PTR, data(c2), Nil));
            drop(c1);
            return result;
        }
        else if (event.type == SDL_KEYDOWN || event.type == SDL_KEYUP)
        {
            n = (struct bn*)malloc(sizeof(struct bn));
            bignum_from_int(n, event.key.keysym.sym);
            NewNumber(n, r2);
            Push(c2, r2);

            any result = cons(CONTEXT_PTR, data(c1), cons(CONTEXT_PTR, data(c2), Nil));
            drop(c1);
            return result;
        }

        any result = cons(CONTEXT_PTR, data(c1), Nil);

        drop(c1);
        return result;
    }
    else
    {
        return Nil;
    }
}

